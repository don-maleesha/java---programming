
public class mathops {

    public static void main(String[] args) {
        
        int a = 5;
        int b = 3;
        int c = 4;

        c = a + b;

        System.out.println("c = " + c);

        int l = 1;
        int m = l + 2;
        int n = m + m;

        System.out.println("n = " + n);

        int x = 3;
        int y = 1;
        int z = 2 + x * 5 - y;

        System.out.println("z = " + z);

    }
}
