public class StringComparison {

    public static void main(String[] args) {
        
        String str1 =  "Hello";
        String str2 = "Hi";

        System.out.println(str1.compareTo(str2)); // compareTo() method compares two strings lexicographically and the output is the difference between the first non-matching characters in the two strings

        System.out.println(str1.equals(str2)); // equals() method compares two strings for equality and returns true if they are equal
          
        }

    }
    

